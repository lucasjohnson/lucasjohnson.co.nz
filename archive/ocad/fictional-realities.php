				<p>
					This body of work is directly influenced by the 2003 Tate Modern exhibition, <a href="http://www.tate.org.uk/whats-on/tate-modern/exhibition/cruel-tender" alt="Tate Modern Website link: Cruel and Tender: The Real in the 20th Century Photograph." target="_blank">Cruel and Tender: The Real in the 20th Century Photograph</a>. This exhibition used the above definition of photographer Walker Evans’ phrase ‘documentary style,’ as it’s central theme. The exhibition explored and examined the photographic work of artists who approached photo-realism as a paradox; where estrangement towards their subjects is shared by a fundamental concern. It is the notion that one must be both empathetic and impassive in order to see a situation for what is. I translated this style into my own practice as the idea that publically expressed social conditions can be witnessed in a clear-eyed manner to create fictional recollections of real and present situations.  
				</p>

				<p>
					Fictional Realities positions itself in densely populated urban areas to observe transactions between people and objects, as well as the locations in which they are found. The images made from these everyday encounters are placed beside one another to tease out details that are often overlooked. Collectively, they transform those depicted into reflections of society, reflections that speak to the larger narratives under which we live: life’s isolation, it's inequalities, conflicts, and indifferences are captured with fleeting instances of hope, desire, and solitude. This series uses pictures of past moments to reflect on current times through documents of anonymous space that contribute to an ongoing allegory of contemporary existence. 
				<p>

